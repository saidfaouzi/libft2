#include "libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*clearing;
	t_list	*clearing2;

	clearing = *lst;
	clearing2 = *lst;
	while (clearing != NULL)
	{
		clearing2 = clearing->next;
		del(clearing->content);
		free(clearing);
		clearing = clearing2;
	}
}
